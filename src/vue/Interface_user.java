    private void jComboBoxTYPEItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxTYPEItemStateChanged
        DefaultTableModel tableModel = (DefaultTableModel) jTableListerSommetPar.getModel();
        compt=0;
        if(jComboBoxTYPE.getSelectedItem().equals("MATERNITE")){
            tableModel.setRowCount(0);
            for(Sommet e:panneauDessin1.getListeSommet()){
                if(e.getType().equals(typeSommet.MATERNITE)){
                String[] newRow={e.getNum()};
               tableModel.addRow(newRow);
               compt++;
                }            
            }
            
        }else if (jComboBoxTYPE.getSelectedItem().equals("CENTRE_DE_NUTRITION")) {
            tableModel.setRowCount(0);
            for(Sommet e:panneauDessin1.getListeSommet()){
                if(e.getType().equals(typeSommet.CENTRE_DE_NUTRITION)){
                String[] newRow={e.getNum()};
               tableModel.addRow(newRow);
               compt++;
                }            
            }
        }else{
            tableModel.setRowCount(0);
            for(Sommet e:panneauDessin1.getListeSommet()){
                if(e.getType().equals(typeSommet.BLOC_OPERATOIRE)){
                String[] newRow={e.getNum()};
               tableModel.addRow(newRow);
               compt++;
                }            
            }
        }
        jLabelNombreDe.setText("Nombre de "+jComboBoxTYPE.getSelectedItem()+" : "+compt);
    }//GEN-LAST:event_jComboBoxTYPEItemStateChanged

