    private void ValiderAjouterBoutonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderAjouterBoutonActionPerformed
        if(jComboBoxAjouter.getSelectedItem().toString().equals("Sommet")){
            typeSommet type = typeSommet.MATERNITE;
            if(jComboBoxTypeAjout.getSelectedItem()=="BLOC_OPERATOIRE"){
                type=typeSommet.BLOC_OPERATOIRE;
            }else if(jComboBoxTypeAjout.getSelectedItem()=="CENTRE_DE_NUTRITION"){
                type=typeSommet.CENTRE_DE_NUTRITION;
            }
            if("".equals(jTextFieldSommetAjout.getText())){
                JOptionPane.showMessageDialog(this, "Il faut mettre un nom de sommet", "Attention", JOptionPane.ERROR_MESSAGE );
            }else{
                panneauDessin1.ajoutSommet(jTextFieldSommetAjout.getText(), type);
                jDialogAjouter.setVisible(false);
            }    
        }else{
            if("".equals(jTextFieldNomAreteAjout.getText())){
                JOptionPane.showMessageDialog(this, "Il faut mettre un nom d'arete", "Attention", JOptionPane.ERROR_MESSAGE );
            }else if("".equals(jTextFieldAjoutFiab.getText())){
                JOptionPane.showMessageDialog(this, "Il faut mettre une fiabilité", "Attention", JOptionPane.ERROR_MESSAGE );
            }else if("".equals(jTextFieldAjoutDist.getText())){
                JOptionPane.showMessageDialog(this, "Il faut mettre une distance", "Attention", JOptionPane.ERROR_MESSAGE );
            }else if("".equals(jTextFieldAjoutDur.getText())){
                JOptionPane.showMessageDialog(this, "Il faut mettre une durée", "Attention", JOptionPane.ERROR_MESSAGE );
            }
            else{
                panneauDessin1.ajoutArete(panneauDessin1.getSommetAt(panneauDessin1.getSommetAvecNom(jComboBoxSommetSrc.getSelectedItem().toString())), panneauDessin1.getSommetAt(panneauDessin1.getSommetAvecNom(jComboBoxSommetDest.getSelectedItem().toString())), jTextFieldNomAreteAjout.getText(), Double.valueOf(jTextFieldAjoutFiab.getText()), Double.valueOf(jTextFieldAjoutDist.getText()), Integer.valueOf(jTextFieldAjoutDur.getText()));
                jDialogAjouter.setVisible(false);
            }
                   
                
               
        }
       
        
        panneauDessin1.mettreAJour();
        Initialisation();
    }//GEN-LAST:event_ValiderAjouterBoutonActionPerformed

