    private void ConfirmerBoutonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmerBoutonActionPerformed
        if(jComboBoxModifier.getSelectedItem().equals("Sommet")){
            typeSommet type = typeSommet.MATERNITE;
            if(jComboBoxModifCarac.getSelectedItem()=="BLOC_OPERATOIRE"){
                type=typeSommet.BLOC_OPERATOIRE;
            }else if(jComboBoxModifCarac.getSelectedItem()=="CENTRE_DE_NUTRITION"){
                type=typeSommet.CENTRE_DE_NUTRITION;
            }
            panneauDessin1.modifsommet(jComboBoxNomModif.getSelectedItem().toString(), type);
            jDialogModifier.setVisible(false);
        }else{
            if("".equals(jTextFieldValeurModif.getText())){
                JOptionPane.showMessageDialog(this, "Il faut mettre une valeur", "Attention", JOptionPane.ERROR_MESSAGE );
            }else{
                if(jComboBoxModifCarac.getSelectedItem().equals("Fiabilité")){
                    panneauDessin1.modifierFiab(Double.valueOf(jTextFieldValeurModif.getText()), jComboBoxNomModif.getSelectedItem().toString());
                    jDialogModifier.setVisible(false);
                }else if(jComboBoxModifCarac.getSelectedItem().equals("Distance")){
                    panneauDessin1.modifierDist(Double.valueOf(jTextFieldValeurModif.getText()), jComboBoxNomModif.getSelectedItem().toString());
                    jDialogModifier.setVisible(false);
                }else{
                    panneauDessin1.modifierDur(Integer.valueOf(jTextFieldValeurModif.getText()), jComboBoxNomModif.getSelectedItem().toString());
                    jDialogModifier.setVisible(false);
                }
            }
        }
        Initialisation();
        panneauDessin1.mettreAJour();
    }//GEN-LAST:event_ConfirmerBoutonActionPerformed

