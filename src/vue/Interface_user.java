    private void ValiderCheminBoutonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderCheminBoutonActionPerformed
        
        jTextAreaResultatChemin.setText("");
        String type=jComboBoxTypeChemin.getSelectedItem().toString();
        String s1=jComboBoxSommetDep.getSelectedItem().toString();
        String s2=jComboBoxSommetFin.getSelectedItem().toString();
        List<String> resultat= new ArrayList<>();
        if ("Fiable".equals(type)){
            resultat=panneauDessin1.cheminfiable(s1,s2);
        }else if("Distance".equals(type)){
            resultat=panneauDessin1.chemindistance(s1,s2);
        }else{
            resultat=panneauDessin1.cheminduree(s1,s2);
        }
        for(int i=0;i<resultat.size();i++){
            if(i+1==resultat.size()){
                jTextAreaResultatChemin.append(resultat.get(i));
            }else
                jTextAreaResultatChemin.append(resultat.get(i) + "->");
        }
        jDialogChemin.setVisible(false);
    }//GEN-LAST:event_ValiderCheminBoutonActionPerformed

